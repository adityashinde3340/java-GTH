public class ARTOPR{
	public static void main(String x[]){
		//Arithmatic Operator 
		// + addition 
		int addNum = 10 , addNum2 = 20;
		System.out.println("addition = "+(addNum+addNum2));
		
		// - Substraction
		int subNum = 10 , subNum2 = 30;
		System.out.println("substraction = "+(subNum-subNum2));
		
		// * Multiplication
		int mulNum = 20 , mulNum2 = 21;
		System.out.println("multiplication = "+(mulNum * mulNum2));
		
		// / Division
		int divNum = 10 , divNum2 = 3;
		System.out.println("division = "+(divNum / divNum2));
		
		// % reminder(mudulus)
		int remNum = 20 , remNum2 = 30;
		System.out.println("reminder = "+(remNum % remNum2));
	}
}